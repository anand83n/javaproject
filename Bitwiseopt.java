public class Bitwiseopt {

    public static void main(String args[]) {

        int a=9,b=12,c;

         c = a & b;
        System.out.println("a&b is " + c );

        c = a | b;
        System.out.println("a|b is " + c );

       c = ~a;
        System.out.println("~a is " + c );
    
    }
}
    

